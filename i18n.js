// Language switching functionality
let currentLanguage = 'en';

// Get user's preferred language from browser or localStorage
function initLanguage() {
  // URL parameter takes priority (e.g. ?lang=es)
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get('lang');
  if (urlLang && translations[urlLang]) {
    currentLanguage = urlLang;
    localStorage.setItem('bws-language', urlLang);
  } else {
    const savedLang = localStorage.getItem('bws-language');
    if (savedLang) {
      currentLanguage = savedLang;
    } else {
      // Detect browser language
      const browserLang = navigator.language || navigator.languages[0];
      if (browserLang.startsWith('es')) {
        currentLanguage = 'es';
      }
    }
  }
  updateLanguage();
  updateToggleButton();
}

// Update all text elements on the page
function updateLanguage() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
      if (element.placeholder !== undefined) {
        // For form inputs with placeholder
        element.placeholder = translations[currentLanguage][key];
      } else {
        // For regular text content
        element.textContent = translations[currentLanguage][key];
      }
    }
  });
  
  // Update HTML lang attribute
  document.documentElement.lang = currentLanguage;
  
  // Update meta tags
  const titleElement = document.querySelector('title');
  if (titleElement) {
    titleElement.textContent = translations[currentLanguage]['meta_title'];
  }
  
  const descriptionMeta = document.querySelector('meta[name="description"]');
  if (descriptionMeta) {
    descriptionMeta.setAttribute('content', translations[currentLanguage]['meta_description']);
  }
}

// Update the toggle button to show active language
function updateToggleButton() {
  const toggleButton = document.getElementById('langToggle');
  if (toggleButton) {
    if (currentLanguage === 'en') {
      toggleButton.innerHTML = '<strong>EN</strong> | ES';
      toggleButton.title = 'Cambiar a Espa√±ol';
    } else {
      toggleButton.innerHTML = 'EN | <strong>ES</strong>';
      toggleButton.title = 'Switch to English';
    }
  }
}

// Toggle between languages
function toggleLanguage() {
  currentLanguage = currentLanguage === 'en' ? 'es' : 'en';
  localStorage.setItem('bws-language', currentLanguage);
  updateLanguage();
  updateToggleButton();
}

// Initialize language when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
  initLanguage();
  
  // Add event listener to language toggle button
  const toggleButton = document.getElementById('langToggle');
  if (toggleButton) {
    toggleButton.addEventListener('click', toggleLanguage);
  }
});